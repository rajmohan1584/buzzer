  void onStartRound() {
    setState(() {
      roundStarted = true;
    });
    showBuzzerToAllClients();
    startRoundTimer();
  }

  void onStopRound() {
    setState(() {
      roundStarted = false;
    });
    stopRoundTimer();
    sendCountdownToAllClients();
    hideBuzzerToAllClients();
    sendTopBuzzersToAllClients();
  }

