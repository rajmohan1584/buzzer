  @override
  void initState() {
    Log.log('Server InitState');
    startMulticastTimer();

    StaticSingleMultiCast.controller2.stream.listen((BuzzMsg msg) {
      // Assert that there is no cross talk.
      if (msg.source == BuzzCmd.server) {
        // Our own message
        return;
      }

      // Messages from a particular client
      BuzzClient? client = clients.findById(msg.sourceId);

      // Race condition
      // Client has an id but server has not processed the registration
      //    to create a client card.
      if (client != null) {
        onClientMessage(msg, client);
        return;
      }

      // Here we process and create a new client card.
      assert(msg.cmd == BuzzCmd.newClientRequest);
      processNewClient(msg);
    });

    super.initState();
  }

  ///////////////////////////////////////////////////////////////////
  ///
  Future processNewClient(
    BuzzMsg msg,
  ) async {
    assert(msg.cmd == BuzzCmd.newClientRequest);
    // ID is generated by the client - so that we can use the same id to send
    final String id = msg.sourceId;
    String name = await GameCache.addNewClient(id);

    // Successful add of new client.
    // Create a new client card.
    setState(() {
      clients.add(name, id);
    });

    // return new server-assigned name
    final BuzzMsg ncResponse = BuzzMsg(
        BuzzCmd.server, BuzzCmd.newClientResponse, {"name": name},
        targetId: id);

    Log.log(ncResponse.toSocketMsg());
    StaticSingleMultiCast.sendBuzzMsg(ncResponse);
    GameCache.dump();
  }

  @override
  void dispose() {
    stopRoundTimer();
    stopMulticastTimer();
    audioPlayer.dispose();
    super.dispose();
  }

